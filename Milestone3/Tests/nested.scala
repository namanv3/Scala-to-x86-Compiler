object FactorialRecursion {
    def nested_if() = {
        var x : Int
        var y : Int
        var z : Int
        var a : Int

        if (x == 0){
            if (y == 0){
                z = 1
            }
            else{
                z = 2
            }
            if (x == 0){
                z = 3
            }
            else{
                z = 4
            }
        }
        else{
            y = 1

            if (a == 0) {
                a = 1
            }
            else {
                a = 11
            }
        }

    }
}
